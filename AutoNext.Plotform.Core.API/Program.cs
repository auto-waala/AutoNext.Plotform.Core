using AutoNext.Plotform.Core.API.Data.Context;
using AutoNext.Plotform.Core.API.Data.UnitOfWork;
using AutoNext.Plotform.Core.API.Middlewares;
using AutoNext.Plotform.Core.API.Services;
using FluentValidation;
using Microsoft.EntityFrameworkCore;
using Serilog;

var builder = WebApplication.CreateBuilder(args);

// ?????????????????????????????????????????????
// 1. Configure Serilog (Console + File only)
//    PostgreSQL sink removed — it blocks startup
//    if DB user lacks CREATE TABLE permission.
//    Re-add it once the app is confirmed working.
// ?????????????????????????????????????????????
Log.Logger = new LoggerConfiguration()
    .ReadFrom.Configuration(builder.Configuration)
    .Enrich.FromLogContext()
    .Enrich.WithMachineName()
    .Enrich.WithThreadId()
    .WriteTo.Console()
    .WriteTo.File("logs/log-.txt", rollingInterval: RollingInterval.Day)
    .CreateLogger();

builder.Host.UseSerilog();

Log.Information(">>> [1] Serilog configured.");

// ?????????????????????????????????????????????
// 2. PostgreSQL DbContext
// ?????????????????????????????????????????????
builder.Services.AddDbContext<ApplicationDbContext>(options =>
    options.UseNpgsql(
        builder.Configuration.GetConnectionString("DefaultConnection"),
        npgsqlOptions =>
        {
            npgsqlOptions.EnableRetryOnFailure(3);
            npgsqlOptions.CommandTimeout(30);
        }));

Log.Information(">>> [2] DbContext configured.");

// ?????????????????????????????????????????????
// 3. Unit of Work
// ?????????????????????????????????????????????
builder.Services.AddScoped<IUnitOfWork, UnitOfWork>();

// ?????????????????????????????????????????????
// 4. Application Services
// ?????????????????????????????????????????????
builder.Services.AddScoped<ILocationService, LocationService>();

// ?????????????????????????????????????????????
// 5. AutoMapper
// ?????????????????????????????????????????????
builder.Services.AddAutoMapper(typeof(Program));

// ?????????????????????????????????????????????
// 6. Redis Cache (falls back to in-memory if
//    Redis connection string is missing)
// ?????????????????????????????????????????????
var redisConnection = builder.Configuration.GetConnectionString("Redis");
if (!string.IsNullOrEmpty(redisConnection))
{
    builder.Services.AddStackExchangeRedisCache(options =>
    {
        options.Configuration = redisConnection;
        options.InstanceName = "AutoNext_";
    });
    Log.Information(">>> [6] Redis cache configured.");
}
else
{
    builder.Services.AddDistributedMemoryCache();
    Log.Warning(">>> [6] Redis not configured — using in-memory cache.");
}

// ?????????????????????????????????????????????
// 7. Controllers + FluentValidation (v11+ API)
// ?????????????????????????????????????????????
builder.Services.AddControllers();
builder.Services.AddValidatorsFromAssemblyContaining<Program>();

Log.Information(">>> [7] Controllers + FluentValidation configured.");

// ?????????????????????????????????????????????
// 8. Swagger
// ?????????????????????????????????????????????
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen(c =>
{
    c.SwaggerDoc("v1", new Microsoft.OpenApi.Models.OpenApiInfo
    {
        Title = "AutoNext Platform Core API",
        Version = "v1",
        Description = "Core data management for AutoNext vehicle marketplace",
        Contact = new Microsoft.OpenApi.Models.OpenApiContact
        {
            Name = "AutoNext Team",
            Email = "support@autonext.com"
        }
    });
    c.EnableAnnotations();
});

Log.Information(">>> [8] Swagger configured.");

// ?????????????????????????????????????????????
// 9. Health Checks
// ?????????????????????????????????????????????
builder.Services.AddHealthChecks()
    .AddNpgSql(builder.Configuration.GetConnectionString("DefaultConnection")!);

// ?????????????????????????????????????????????
// 10. CORS
// ?????????????????????????????????????????????
builder.Services.AddCors(options =>
{
    options.AddPolicy("AllowAll", policy =>
    {
        policy.AllowAnyOrigin()
              .AllowAnyMethod()
              .AllowAnyHeader();
    });
});

// ?????????????????????????????????????????????
// 11. Response Caching
// ?????????????????????????????????????????????
builder.Services.AddResponseCaching();

Log.Information(">>> [11] All services registered. Building app...");

// ?????????????????????????????????????????????
// Build
// ?????????????????????????????????????????????
var app = builder.Build();

Log.Information(">>> [12] App built. Configuring middleware pipeline...");

// ?????????????????????????????????????????????
// Middleware Pipeline
// ?????????????????????????????????????????????

// Swagger available in all environments for now.
// Lock to Development only once confirmed working:
// if (app.Environment.IsDevelopment()) { ... }
app.UseSwagger();
app.UseSwaggerUI(c =>
{
    c.SwaggerEndpoint("/swagger/v1/swagger.json", "AutoNext Platform Core API v1");
    c.RoutePrefix = "swagger"; // http://localhost:5096/swagger
});

Log.Information(">>> [13] Swagger UI configured.");

app.UseSerilogRequestLogging();

app.UseMiddleware<GlobalExceptionMiddleware>();

// HTTPS redirect disabled — causes hang without a trusted dev cert.
// To re-enable: run `dotnet dev-certs https --trust` then uncomment:
// app.UseHttpsRedirection();

app.UseCors("AllowAll");

app.UseResponseCaching();

app.UseAuthorization();

app.MapControllers();

app.MapHealthChecks("/health");

Log.Information(">>> [14] Middleware pipeline configured.");

// ?????????????????????????????????????????????
// Apply Migrations
// ?????????????????????????????????????????????
try
{
    Log.Information(">>> [15] Applying database migrations...");
    using var scope = app.Services.CreateScope();
    var dbContext = scope.ServiceProvider.GetRequiredService<ApplicationDbContext>();
    await dbContext.Database.MigrateAsync();
    Log.Information(">>> [15] Database migrations applied successfully.");
}
catch (Exception ex)
{
    Log.Error(ex, ">>> [15] Migration failed — app will still start.");
}

// ?????????????????????????????????????????????
// Start
// ?????????????????????????????????????????????
Log.Information(">>> [16] Calling app.Run() — open http://localhost:5096/swagger");
Log.CloseAndFlush();

app.Run();